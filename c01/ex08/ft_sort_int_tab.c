void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	tmp;

	while (--size > 0)
	{
		i = 0;
		while (i < size)
		{
			if (tab[i] > tab[i + 1])
			{
				tmp = tab[i + 1];
				tab[i + 1] = tab[i];
				tab[i] = tmp;
			}
			i++;
		}
	}
}
